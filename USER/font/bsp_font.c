void EN_Char(uint16_t Xpos, uint16_t Ypos, uint32_t Current_Addr,uint32_t Font_Color , uint32_t Groung_Color, char *ptr)
{  
  uint16_t charater;
  /* Send the string character by character on lCD */
  while (*ptr != '\0')
  {
		charater = *(uint16_t *)ptr;
    /* Display one character on LCD */
    dis_english(Xpos, Ypos,Current_Addr,Font_Color, Groung_Color,charater);
    /* Decrement the column position by width */
    Xpos += 16;
    /* Point on the next character */
    ptr++;
  }
}


static void GetGBKCode_from_EXFlash( uint8_t * pBuffer, uint16_t charater)
{ 
	uint32_t Addr;
	uint8_t CodeH,CodeL;
	CodeH = (charater&0xff00)>>8;
	CodeL = (charater&0x00ff);

	/*GB2312 ¹«Ê½*/
  Addr = (((CodeH-0xA0-1)*94) +(CodeL-0xA0-1))*24*24/8;

	Addr += Standard_Address;

	SPI_FLASH_BufferRead(pBuffer,Addr,24*24/8);  
}



static void dis_char(uint16_t Xpos, uint16_t Ypos, uint32_t Current_Addr,uint32_t Font_Color , uint32_t Groung_Color ,uint16_t charater)
{
	GetGBKCode_from_EXFlash(charaterDataBuffer,charater);
	
	uint32_t *p = ((uint32_t *)Current_Addr + (LCD_PIXEL_WIDTH*Ypos + Xpos));
	
	uint8_t i,j;
	for(i=0;i<24;i++)
	{
		for(j=0;j<8;j++)
		{
			if( ((charaterDataBuffer[3*i]<<j)&0x80) )
				*p = Font_Color;
			else
				*p = Groung_Color;
			p++;
		}
			for(j=0;j<8;j++)
		{
			if( ((charaterDataBuffer[3*i+1]<<j)&0x80) )
				*p = Font_Color;
			else
				*p = Groung_Color;
			p++;
		}
			for(j=0;j<8;j++)
		{
			if( ((charaterDataBuffer[3*i+2]<<j)&0x80) )
				*p = Font_Color;
			else
				*p = Groung_Color;
			p++;
		}
		p += (LCD_PIXEL_WIDTH - 24);
	}
}



void CH_Char(uint16_t Xpos, uint16_t Ypos, uint32_t Current_Addr,uint32_t Font_Color , uint32_t Groung_Color,char *pString)
{
	uint16_t charater;
	
	while(*pString != '\0')
	{
		charater = *(uint16_t *)pString;
		
		charater =  ((charater&0x00FF)<<8) | ((charater&0xFF00)>>8) ;
		
		dis_char(Xpos,Ypos,Current_Addr, Font_Color ,  Groung_Color,charater);
		
		Xpos += 24;
		
		pString += 2;
	}
}




